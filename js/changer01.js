function change(elmnt) {
    switch(elmnt.innerHTML){
	    case "A":
	        elmnt.innerHTML = "2";
	        break;
	    case "2":
	        elmnt.innerHTML = "A";
	        break;
	    case "N":
	        elmnt.innerHTML = "7";
	        break;
	    case "7":
	        elmnt.innerHTML = "N";
	        break;
	    case "3":
	        elmnt.innerHTML = "P";
	        break;
	    case "P":
	        elmnt.innerHTML = "3";
	        break;
	    case "4":
	        elmnt.innerHTML = "E";
	        break;
	    case "E":
	        elmnt.innerHTML = "4";
	        break;
	    }

}
